class Car {
  final int id;
  final int year;
  final String make;
  final String model;
  final String price;
  final String mileage;
  final String type;

  Car({
    required this.id,
    required this.year,
    required this.make,
    required this.model,
    required this.price,
    required this.mileage,
    required this.type,
  });
}
